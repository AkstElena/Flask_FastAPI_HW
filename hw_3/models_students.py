    lastname = db.Column(db.String(20), nullable=False)
    group = db.Column(db.String(10), nullable=False)
    email = db.Column(db.String(50))
    id_faculty = db.Column(db.Integer, db.ForeignKey('faculty.id'), nullable=False)
